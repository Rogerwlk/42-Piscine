char	*ft_rot42(char *str)
{
	char	*copy;

	copy = str;
	while (*copy)
	{
		if (*copy >= 'A' && *copy <= 'Z')
		{
			*copy = ((*copy - 'A') + 42) % 26 + 'A';
		}
		else if (*copy >= 'a' && *copy <= 'z')
		{
			*copy = ((*copy - 'a') + 42) % 26 + 'a';
		}
		copy++;
	}
	return (str);
}

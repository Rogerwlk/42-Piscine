void	swap(int *a, int *b)
{
	int temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

int		ft_button(int i, int j, int k)
{
	if (i > j)
		swap(&i, &j);
	if (i > k)
		swap(&i, &k);
	if (j > k)
		swap(&j, &k);
	return (j);
}

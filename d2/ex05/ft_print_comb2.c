int		ft_putchar(char c);

void	ft_print_comb2(void)
{
	int i;

	i = 0;
	while (i <= 9999)
	{
		if (i / 100 < i % 100 && i / 10 % 10 <= i % 10)
		{
			ft_putchar(i / 1000 + '0');
			ft_putchar(i / 100 % 10 + '0');
			ft_putchar(' ');
			ft_putchar(i / 10 % 10 + '0');
			ft_putchar(i % 10 + '0');
			if (i / 100 < 98)
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
		}
		i++;
	}
}

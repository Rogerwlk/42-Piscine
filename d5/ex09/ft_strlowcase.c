char	*ft_strlowcase(char *str)
{
	char *copy;

	copy = str;
	while (*copy != '\0')
	{
		if (*copy >= 'A' && *copy <= 'Z')
			*copy += ('a' - 'A');
		copy++;
	}
	return (str);
}

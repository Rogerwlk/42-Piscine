char	*ft_strupcase(char *str)
{
	char *copy;

	copy = str;
	while (*copy != '\0')
	{
		if (*copy >= 'a' && *copy <= 'z')
			*copy -= ('a' - 'A');
		copy++;
	}
	return (str);
}

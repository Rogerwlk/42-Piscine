char	*ft_strcat(char *dest, char *src)
{
	int len;

	len = 0;
	while (dest[len])
		len++;
	while (*src)
	{
		dest[len] = *src;
		src++;
		len++;
	}
	dest[len] = '\0';
	return (dest);
}

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char *copy;

	copy = dest;
	while (n > 0 && *src != '\0')
	{
		*copy = *src;
		copy++;
		src++;
		n--;
	}
	while (n > 0)
	{
		*copy = '\0';
		copy++;
		n--;
	}
	return (dest);
}
